    private void ajouterBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ajouterBtnMouseClicked
        if(idField.getText().length() == 0){JOptionPane.showMessageDialog(null,"Veuiller completer le champ du ID !");
        }else{if(nomField.getText().length() == 0){JOptionPane.showMessageDialog(null,"Veuiller completer le champ du nom !");
            }else{if(adresseField.getText().length() == 0){JOptionPane.showMessageDialog(null,"Veuiller completer le champ d'adressee !");
                }else{if(phoneField.getText().length() == 0){JOptionPane.showMessageDialog(null,"Veuiller completer le champ du telephone !");
                    }else{if(cinField.getText().length() == 0){JOptionPane.showMessageDialog(null,"Veuiller completer le champ du CIN !");
                        }else{if((int)nbrPlace.getValue() == 0){JOptionPane.showMessageDialog(null,"Nombre de place invalide !");
                            }else{if((int)montant.getValue() == 0){JOptionPane.showMessageDialog(null,"Montant invalide !");
                                }else{if(dateVoyage.getText().length() == 0){JOptionPane.showMessageDialog(null,"Veuiller completer le champ date voyage !");
                                }else{if(heureDepart.getText().length() == 0){JOptionPane.showMessageDialog(null,"Veuiller completer le champ heure depart !");
                                    }else{if(villeArrivee.getSelectedItem() == villeDepart.getSelectedItem()){JOptionPane.showMessageDialog(null,"Selectionnez differentes ville depart et arrivée ! ");
                                        }else{
                                           String sexe = sexeRadio1.isSelected() ? "M" : "F" ;
                                            String classeVoyage = null ;
                                            if (classe1.isSelected()){
                                                classeVoyage = "Premier classe" ;
                                            } else if(classe2.isSelected()){
                                                classeVoyage = "Deuxieme classe" ;
                                            } else if(classeVIP.isSelected()){
                                                classeVoyage = "VIP classe";
                                            }
                                            String nb = nbrPlace.getValue().toString();
                                            String idClient = idField.getText() ;
                                            int total = (int)nbrPlace.getValue() * (int)montant.getValue() ;
                                            Client c1 = new Client(idField.getText(),nomField.getText(),prenomField.getText(),sexe,adresseField.getText(),cinField.getText(), phoneField.getText());
                                            Reservation r1 = new Reservation(nb, classeVoyage, villeDepart.getSelectedItem().toString(), villeArrivee.getSelectedItem().toString(), dateVoyage.getText(), heureDepart.getText(),montant.getValue().toString(),String.valueOf(total) , idClient) ;
                                            try {
                                                gestion.GestionClient gp = new GestionClient();
                                                gp.insert(c1);
                                                JOptionPane.showMessageDialog(null,"Ajout du client reussi !");
                                            }catch(Exception e){
                                                System.err.println("errreur : " + e);
                                            } 
                                            try {
                                                gestion.GestionReservation gr = new GestionReservation();
                                                gr.insert(r1);
                                                JOptionPane.showMessageDialog(null,"Ajout du reservation reussi !");
                                            }catch(Exception e){
                                                System.err.println("errreur : " + e);
                                            } 
                                        }  
                                    }
                                }
                            }
                        }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_ajouterBtnMouseClicked

